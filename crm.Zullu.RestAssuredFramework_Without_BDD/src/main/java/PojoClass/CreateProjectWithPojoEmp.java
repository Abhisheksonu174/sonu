package PojoClass;

public class CreateProjectWithPojoEmp {
	String eName;
	String place;
	String email;
	
	
	public CreateProjectWithPojoEmp(String eName, String place, String email) {
		
		this.eName = eName;
		this.place = place;
		this.email = email;
	}


	public String geteName() {
		return eName;
	}


	public String getPlace() {
		return place;
	}


	public String getEmail() {
		return email;
	}
	
	
	

}
